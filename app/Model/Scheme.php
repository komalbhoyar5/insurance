	function checkUniquetitle($data) {
		if (isset($this->data[$this->alias]['id'])) {
		    $isUnique = $this->find(
		                'first',
		                array(
		                    'fields' => array(
		                        'Scheme.id',
		            'Scheme.title'
		                    ),
		                    'conditions' => array(
		                        'Scheme.title' => $this->data[$this->alias]['title'],
		                        'Scheme.id !=' => $this->data[$this->alias]['id']
		                    )
		                )
		        );
		}else{
			$isUnique = $this->find(
	                'first',
	                array(
	                    'fields' => array(
	                        'Scheme.id',
		            'Scheme.title'
		                    ),
		                    'conditions' => array(
		                        'Scheme.title' => $this->data[$this->alias]['title']
		                    )
		                )
	        	);
		}
	    
	    if(!empty($isUnique)){
	    	return false;
	    }else{
	        return true; //If there is no match in DB allow anyone to change
	    }
    }
